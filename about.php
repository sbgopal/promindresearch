<h2>Editorial Process</h2>
<p>The editorial process of <em>PMRP Journal of Engineering Research &ndash; Part A: Computational Engineering</em> ensures that all manuscripts are evaluated fairly, transparently, and according to the highest academic and ethical standards.</p>
<ol>
<li><strong> Submission</strong></li>
</ol>
<ul>
<li>Authors submit manuscripts via the <strong>online submission system</strong> or by email to <strong>editor@pmrpjer.com</strong>.</li>
<li>All submissions must follow the <strong>journal template</strong> and <strong>APA citation style</strong>.</li>
</ul>
<ol start="2">
<li><strong> Initial Screening</strong></li>
</ol>
<ul>
<li>The Editor-in-Chief or an Associate Editor performs a <strong>preliminary check</strong> to ensure:
<ul>
<li>The manuscript fits within the <strong>Aim &amp; Scope</strong> of the journal.</li>
<li>Compliance with formatting and ethical guidelines.</li>
<li>Plagiarism screening (using appropriate software).</li>
</ul>
</li>
<li>Manuscripts failing the initial screening are <strong>returned to authors</strong> for revision or rejected.</li>
</ul>
<ol start="3">
<li><strong> Peer Review (Double-Blind)</strong></li>
</ol>
<ul>
<li>Suitable manuscripts are assigned to <strong>expert reviewers</strong>.</li>
<li>The review is <strong>double-blind</strong>:
<ul>
<li>Authors&rsquo; identities are concealed from reviewers.</li>
<li>Reviewers&rsquo; identities are concealed from authors.</li>
</ul>
</li>
<li>Reviewers evaluate:
<ul>
<li>Originality and novelty of the work.</li>
<li>Technical quality and validity of results.</li>
<li>Clarity of presentation.</li>
<li>Relevance to the journal&rsquo;s scope.</li>
</ul>
</li>
</ul>
<ol start="4">
<li><strong> Editorial Decision</strong></li>
</ol>
<ul>
<li>Based on reviewer feedback, the editor may decide:
<ul>
<li><strong>Accept without revision</strong></li>
<li><strong>Minor revision</strong></li>
<li><strong>Major revision</strong></li>
<li><strong>Reject</strong></li>
</ul>
</li>
<li>Authors are notified of the decision along with reviewer comments.</li>
</ul>
<ol start="5">
<li><strong> Revision &amp; Resubmission</strong></li>
</ol>
<ul>
<li>Authors must address all reviewer comments and submit a <strong>revised manuscript</strong> within the given timeline.</li>
<li>Revised manuscripts may be sent for a second round of review if necessary.</li>
</ul>
<ol start="6">
<li><strong> Final Acceptance</strong></li>
</ol>
<ul>
<li>Once the revised manuscript meets all editorial and technical requirements, the Editor-in-Chief issues a <strong>formal acceptance letter</strong>.</li>
</ul>
<ol start="7">
<li><strong> Production &amp; Publication</strong></li>
</ol>
<ul>
<li>Accepted manuscripts are copyedited, typeset, and assigned a <strong>DOI</strong>.</li>
<li>Articles are published online in the next scheduled issue or as <strong>Online First</strong>.</li>
</ul>
<ol start="8">
<li><strong> Post-Publication</strong></li>
</ol>
<ul>
<li>Published articles are indexed in the journal&rsquo;s listed databases.</li>
<li>Authors are encouraged to share and promote their work to increase visibility and citations.</li>
</ul>
