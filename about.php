<h2>Publication Ethics & Professional Standards</h2>

<p><em>PMRP Journal of Engineering Research &ndash; Part A: Computational Engineering</em> follows the <strong>COPE Core Practices</strong> and expects all authors, reviewers, and editors to maintain the highest standards of integrity.</p>
<p><strong>Human &amp; Animal Research</strong></p>
<ul>
<li>Research involving data from humans or animals must have prior approval from a recognized ethics committee, with details stated in the manuscript.</li>
<li>Clinical trials must include registration details; human studies require informed consent.</li>
<li>Animal research must follow relevant welfare guidelines.</li>
</ul>
<p><strong>Data &amp; Originality</strong></p>
<ul>
<li>All submissions must be original, unpublished, and not under review elsewhere.</li>
<li>Authors must present accurate methods and results, retain raw data, and make it available if requested.</li>
<li>Duplicate publication, plagiarism, fabrication, falsification, or image manipulation is strictly prohibited.</li>
</ul>
<p><strong>Conflicts of Interest</strong></p>
<ul>
<li>All potential conflicts must be disclosed at submission.</li>
<li>Editors and reviewers must provide unbiased, confidential evaluations.</li>
</ul>
<p><strong>Misconduct &amp; Sanctions</strong></p>
<ul>
<li>Proven ethical breaches may result in rejection, retraction, sanctions, or reporting to relevant authorities.</li>
</ul>
<p>The journal uses plagiarism detection software (iThenticate) and reserves the right to act in accordance with COPE guidelines in all cases of suspected misconduct.</p>
